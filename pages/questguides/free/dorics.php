<b>2004HQ - Doric's Quest</b><br>
<br>
Description: Doric the dwarf is happy to let you use his anvils but first he would like you to run an errand for him.<br>
<br>
<b>Difficulty Level: <font color="Green">Easy</font></b><br>
<br>
Items/Skills Needed To Start: Any Pickaxe, 15 mining.<br>
<br>
Starting Location: Doric's Anvil (north of Falador)<br>
<br>
Reward: 1 quest point, 1300 mining exp, 180 gold coins, permission to use Doric's anvils.<br>

<br>
Instructions:<br>
<br>
1. First off go talk to Doric who is located North-West of Falador, and he will tell you that to use his anvils he wants 6 Clay, 4 Copper ore, and 2 Iron ore.<br>
<br>
2. If you already have a pickaxe skip this step. If you don't have one yet, walk over to Barbarian Village and in one of the buildings you will see a Bronze pickaxe on a table. Pick it up.<br>
<br>
3. Ok, so you have your pickaxe with you, walk over to the northern entrance of the Dwarven Mines which is located North-East of Falador.<br>
<br>
4. Once you get there go down the trap door and go south until you see some iron and copper rocks. Mine 2 Iron ore and 4 Copper ore from them. Then go south a little bit more and you will see some clay rocks. Mine 6 of these.<br>
<br>
5. Now you have your 6 Clay, 4 Copper ore, and 2 Iron ore in your inventory. Return to Doric and give him the ores. He will appreciate it very much and will reward you with 1 quest point, 180 coins, and approximately 1,300 mining experience. <br>
<br>
6. Congratulations you have just finished Doric's Quest and can now use his anvils.<br>
