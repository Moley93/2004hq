<b>2004HQ - The Grand Tree</b><br>
<br>
Description: The Grand Tree, which shelters the majority of RuneScape's small gnome population, is dying. Is it human sabotage or an inside job? Help King Shareem to find the true cause and save the tree gnomes from an uncertain fate.<br>
<br>
<b>Difficulty Level: <font color="Red">Hard</font></b><br>
<br>
Items/Skills needed to start: Level 25 agility, able to beat level 172 demon, , and 30gp for your trip to Ardougne from Brimhaven<br>
<br>
Starting Location: Grand Tree in the Gnome Stronghold northwest of Ardougne.<br>
<br>
Reward: 11200 Agility experience (at level 30 you get 4 levels), 18400 attack experience, 2150 mage experience, and 5 quest points. Ability to use spirit tree teleport from gnome stronghold (assuming tree gnome village quest is complete)" Also, you are able to use the gnome gliders after you complete this quest.<br>
<br>
Instructions:<br>
<br>
Talk to the King at the Grand Tree, which is in the northern area of the Gnome Stronghold; he will give you a translation book and a bark sample.<br>
<br>
<img src="img\questimages\grandtree1.png" height="280" width="400"><br>
<br>
Take these to Hazelmere; he is south of Ardougne and east of Yannile, on a spit of land near the town with the fight arena and Yanille (magic guild town).<br>
<br>
<img src="img\questimages\grandtree2.png" height="350" width="400"><br>
<br>
There are Jungle Spiders on this peninsula. The house is on top of hill. Enter and go up the ladder.<br>
Then talk to Hazelmere; he will take the sample and show you a message.<br>
The message when translated using your book says:<br>
“A man came to me with the King seal<br>
I gave human Daconia rock<br>
and Daconia rocks will kill trees"<br>
* I have given a direct translation. *<br>
Go back to the King at Grand Tree, talk to him, and select the message as above (you will have to select 'none of the above' 3 times before the correct selection appears).<br>
<br>
Go to Glough (in tree south and slightly southeast of path), talk to him, and he says he will take care of it and you should go back to talk to king.<br>
<br>
<img src="img\questimages\grandtree3.png" height="300" width="400"><br>
<br>
The King tells you that they have already caught someone; you ask to see the prisoner. The King gives you permission and tells you that the prisoner is at the top of tree.<br>
<br>
Charlie, the prisoner, tells you to search Glough's home.<br>
<br>
Go back to Glough, search the cupboard; you will find a journal. (There is also a chest; you will get the key to this a little later in the quest).<br>
<br>
Take the journal back to the King and tell him of your suspicions. The King dismisses your ideas and sends you back to talk to Glough.<br>
<br>
Talk to Glough once again... you accuse him, and he has you arrested and imprisoned next to Charlie at the top of the tree.<br>
<br>
Whilst in prison Charlie tells you about warships being built on Karamja. He tells you a password to tell to the foreman of the shipyard.<br>
<br>
The King lets you out of prison; you can now use the glider to go to Karamja to find the foreman. (Jungle spiders and Jogres in this area.)<br>
<br>
Try to open the gate; the shipyard worker (level 44) will ask for a password. Select the three choices:<br>
<br>
ka<br>
lu<br>
min<br>
<br>
<img src="img\questimages\grandtree4.png" height="300" width="400"><br>
<br>
Talk to the foreman; he will ask 3 questions to make sure you've come from Glough.<br>
1. How is Glough’s wife? (She is no longer with us)<br>
2. What’s Glough’s favourite food? (Wormhole)<br>
3. What is his new girlfriend called? (Anita)<br>
(If you get the answers wrong he will attack you and you will have to start again with a new foreman!)<br>
After this you will receive an a note for Glough about the ship. Take this back to the King.<br>
<br>
You can't return by the glider, as it was broken when you landed. If you try to walk back the guards won't let you back in as they think you are a spy. Instead go to 'Gnome Maze Village' and transport back in using the spirit tree.<br>
<br>
<img src="img\questimages\grandtree5.png" height="300" width="400"><br>
<img src="img\questimages\grandtree6.png" height="300" width="400"><br>
<br>
The King yet again refuses to believe your accusations against Glough.<br>
Go and talk to Charlie at the top of the tree again.<br>
<br>
Charlie suggests that you talk to Glough’s girlfriend, Anita; she might be able to help you. Anita lives near the swamp, northwest of the Grand Tree.<br>
<br>
Talk to Anita and she will ask you to return a key to Glough. This is the key to the chest that you have been waiting for!<br>
<br>
Open the chest in Glough’s house, you will get some notes about his plan to take over Runescape.<br>
<br>
Take this evidence to the King. Once again, he doesn't believe you but he says that they have searched Glough's house and all they found was four sticks. The King gives you the sticks. They spell T-U-Z-O.<br>
<br>
If you check with your translation book you will find that, if rearranged, the sticks will spell the word 'open'.<br>
<br>
For the whole list of translations go to the bottom of this guide<br>
<br>
Go to Glough’s house and go up to the watchtower (this is why you need level 25 agility to complete quest).<br>
<br>
Here you will find a stone stand. Place the sticks on stand in the following order: (use the sticks with stand)<br>
't' in the far left position<br>
'u' in center left position<br>
'z' in center right<br>
and finally the 'o' in the far right.<br>
<br>
Once you have done this, open the stone stand...be warned before you do this…<br>
After you drop down a Black Demon (level 172) attacks you. So be sure to go with plenty of prayer to paralyze or food!<br>
